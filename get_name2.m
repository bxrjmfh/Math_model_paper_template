function name1 = get_name2(d)
    % k_means 聚类铅钡玻璃输出结果
    name = ["高Cu玻璃"
    "Al-Ca玻璃"
    "Al-Na玻璃"
    "高Al玻璃"
    "Al-P玻璃"
    ];
    
    center = [2.335433402,1.478130452,6.786315919,0.678425331,0;
    3.0336531,1.222209651,0.628671112,0.634294789,0.564210526;
    2.958500324,0.555666163,1.460449151,0.34774445,6.463204532;
    13.45427049,2.037111063,0.39,0.222032987,2.204629398;
    5.894344967,2.161219437,1.286819712,3.650787352,-2.22E-16;
    ]; % 聚类中心

    idx = [6,4,8,11,2];

    d = d(idx);
    dis = zeros(5,1);
    for h = 1:5
        dis(h) = sum((d-center(h,:)).^2);
    end
    [~,id] = min(dis);
    id
    name1 = name(id);
end